        <h1>Department of Mechanical Engineering</h1>

         <p>The department of Mechanical Engineering was started in 2009 and has grown multifold & currently offers diploma course in Mechanical Engineering with an intake of 60 students, UG course in Mechanical Engineering with an intake of 120 students, PG courses in Machine Design and Computer Integrated Manufacturing with an intake of 18 students each. From the next academic year, the department will also be offering  a diploma course in Mechanical Engineering and M.Tech in Computer Integrated Manufacturing.</p> 

        <h3>Our Vision</h3>

<p><strong>"The Mechanical Engineering Department strives to be recognized globally for outstanding education and research leading to well-qualified engineers, who are innovative, entrepreneurial and successful in advanced fields of engineering and research."</strong></p>

        <h3>Our Mission</h3>

        <ul>

<li>Imparting quality education to the students and enhancing their skills to make them globally competitive mechanical engineers.</li>

<li>Maintaining vital, state-of-the-art research facilities to provide its students and faculty with opportunities to create, interpret, apply and disseminate knowledge.</li>

<li>To develop linkages with world class R&D organizations and educational institutions in India and abroad for excellence in teaching, research and consultancy practices.</li>

</ul>



