        <h1>Department of Basic Science & Humanities</h1>

        <p>The department of Basic Science & Humanities is highly focused on imparting quality education to meet the global challenges of science and technology. The department  plays a  vital role in laying the strongest foundation, so that the students are well equipped to stand up to higher education.</p>

        <p>The department comprises of faculty members from various disciplines such as Mathematics , Physics, Chemistry and Humanities. The humanities faculty impart language training (English and Kannada) and training  in soft skills. The Department also imparts knowledge on subjects like Environmental studies and constitution of India  and professional  ethics to the undergraduate engineering students which is a part of VTU curriculum.</p>

        <p>Basic Sciences describes the most basic objects, forces, relations between them and laws governing them. All Engineering branches have evolved from these basic principles, the branches of physics, chemistry and mathematics from an important base in understanding the larger concepts and theories of the various Engineering branches. The Department workshops and labs are geared to handle all requirements of the first year .The Department of mathematics has one of the best results among the VTU colleges in Bangalore</p>

        <p>The department of Mathematics also conducts courses in Discrete Mathematical Structures, Graph Theory & Combinatorics and advanced mathematics for diploma students. </p>

        <p>Students requiring extra help are identified and special remedial classes are conducted   for them to enable them to perform well. With sincere efforts of the team, the department has been consistently obtaining excellent results.</p>

       

        <h3>Mission</h3>

        <p>Making Engineers to develop the mathematical & scientific thinking and applying it to solve complex engineering problems, designing mathematical modeling for systems involving global level technology.</p>

        <h3>Vision</h3>

