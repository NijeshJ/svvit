        <h1>M.Tech. in Construction Technology </h1>

<p>Construction Technology involves the study of construction to successfully achieve the structural design with recommended specifications and conditions of contract. It also includes study of geo-technics, construction equipments, and temporary works like scaffolding, false work and formwork etc. required to facilitate the construction process conforming to health and safety regulations. Construction technology also includes study of latest erection and fabrication processes.



</p>

<p>The modern trend is to construct lighter and taller buildings, to achieve this there is a need to have sophisticated equipments employed in the construction process. Excavation of foundation is a challenging task in an area surrounded by existing buildings and a busy road. All of these factors should be considered while estimating the cost of the construction project. Construction technologist work very closely with construction managers and the quantity surveying professionals. Construction technologist should also have a good knowledge about the different types of materials used in construction along with the testing procedures to assure quality control of these materials on site.





</p>



