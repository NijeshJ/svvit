        <h1>B.E. in Electronics and Communications</h1>

        <p>The Electronics and Communication Engineering is a wing of engineering that deals with the effects and behavior of electrons to develop system, device, components or equipments, integrated circuits that use electricity as the driving force. It also includes instrumentation engineering, telecommunication, semiconductor circuit design and power engineering. Electronics engineering has a major role in improving productivity in industries like oil, energy and agriculture. In steel, petroleum and chemical industries it is the electronic devices that direct, control and test production processes. Health care industry depends on electronic instruments to perform chemical tests and to check body functions.</p>

<p>The Analog Electronics Laboratory, Logic Design Laboratory, Microcontrollers Laboratory, HDL Laboratory, DSP Laboratory, Analog Communication Laboratory and the Research Laboratory have been developed with a contemporary design. The department's experienced and devoted faculty brings out the best in the student folk.</p>





