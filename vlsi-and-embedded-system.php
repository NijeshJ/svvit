<p><strong>About the Programme:</strong></p>



<p>The Department of Electronics and Communication Engineering, started M.Tech in VLSI design and Embedded systems in the year 2014. The program is approved by AICTE , Government of Karnataka and is affiliated to Visvesvaraya Technological University, Belgaum.</p>



<p>Advances in VLSI have today enabled most systems to become compact, highly reliable and deliver data at high speed. Further the advances facilitate the designer to tailor the IC for specific applications (Application Specific Integrated Circuits). Requirement of improved solutions for miniaturization, high speed, reliability, and high performance computers, necessitates skilled engineers. Thus there is an urgent need to produce quality engineers who can conceive, design and develop VLSI and Embedded systems.</p>



<p>The programme curriculum is aimed at VLSI system design (both Front end & Back end designs) covering algorithms, Hardware description Languages, System Architectures, Physical designs, Verification techniques, Simulation & Synthesis, Low power design techniques and Mixed mode design methodologies. The course also offers strong knowledge in the Embedded system design by covering thrust areas such as Advanced Embedded Microcontrollers, Real Time Embedded Systems, Advanced Embedded System Design and System On Chip. Students also have an access to industry collaborative laboratories set up by Texas Instruments, Nxp Semiconductors and Freescale semiconductors.</p>



<p><strong>Eligibility:</strong> Candidates who have acquired BE/B.Tech/AMIE degree in Electronics & Communication, Telecommunication Engineering, Electrical and Electronics Engineering, Instrumentation Engineering, Bio-Medical Engineering and Medical Electronics by securing not less than 50% marks in aggregate are eligible. However in case of candidates belonging to SC/ST/Group 1 aggregate percentage of marks in qualifying examination should not be less than 45%. The admissions are through central counseling on the basis of merit in PGCET or GATE. GATE qualified candidates are eligible for scholarship through AICTE. A few seats are also available for sponsored and Management candidates.</p>



<p><strong>Infrastructure:</strong> The Department has dedicated Laboratories equipped with Electronics Design Automation (EDA) Tools such as (1) Cadence (2) Model-Sim (3)Synthesis tool from Xilinx (4) FPGA trainer Kits. and (5) RTOS tool .</p>



<p><strong>Areas of Research:</strong> The students will get an opportunity to participate in projects related to, but not limited to: Design of Low power VLSI system, Optimization of Digital circuits, Synthesis of Digital Circuits, Testing of VLSI circuits, Design of Real time Embedded systems and large area flexible electronics</p>



