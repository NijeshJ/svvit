        <h1>Research and Product Innovation Cell</h1>

        <p>The Research and Product Innovation Cell (aka R&D Center) aims at imparting inventive and research oriented engineering education to students by enabling them to become capable of solving practical problems of society with innovative methods and processes. As mere theoretical engineering education with standard laboratory work is insufficient to make the students employable by the time they complete their curricular study, the center aims at  honing  their independent thinking ability, problem solving skills, and entrepreneurial skills so as to empower them  to contribute towards the economical development of the country. Students are trained for conducting practical research, and for developing useful products starting from conceptual ideation up to prototype completion.</p>
<p>The director of the center, Dr. Dinesh K. Anvekar has years of experience working in both Academia ( 16 years in the Indian Institute of Science) and Industry (8 years in Honeywell, 2 years in IBM Research Center), and 4 years of entrepreneurial training experience in VTU affiliated engineering institutes. The center is well equipped with tools and computer hardware and software systems for conducting university level research work and developing prototypes of products.</p>

