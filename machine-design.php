<p>The program mainly focuses on Theory of Elasticity and Plasticity, Theory of Plates and Shells, Advanced Vibrations, Finite Element Analysis, Kinematics Design, Tribology and Bearing Design, Fracture Mechanics, Experimental Stress Analysis, etc. Students have opportunity to work in the areas of design and analysis in association with the on-going research projects of the faculty members sponsored by agencies such as DRDO laboratories, ISRO, AICTE, DST and other agencies.M.Tech. in Machine Design was started in the year 2012 in the department of Mechanical Engineering. The program is approved by AICTE, Government of Karnataka and is affiliated to Visvesvaraya Technological University, Belagauma. The program curriculum involves Analysis of static and dynamic forces to design machine elements to meet strength, safety, reliability and cost specifications. The program focuses on Design of Mechanisms and Components using Analytical, Numerical and Experimental approaches. It aims to provide knowledge of behavior of materials and systems in both elastic and plastic ranges, failure modes and mechanisms in both static and dynamic conditions.</p>



<p><strong>Eligibility:</strong> Candidates who have acquired BE/B.Tech or equivalent degree in Mechanical Engineering/ Industrial Engineering and Management/ Industrial Production Engineering/ Automobile Engineering/ Manufacturing Engineering by securing not less than 50% marks in aggregate. However in case of candidates belonging to SC/ST/Group 1 aggregate percent of marks in qualifying examination should not be less than 45%. The admissions are through central counseling on the basis of merit in PGCET or GATE. GATE qualified candidates are eligible for scholarship through AICTE.</p>



<p><strong>Infrastructure:</strong> The program is supported with: Computer Aided Engineering (CAE) Laboratory (ANSYS, LS-Dyna, Abacus), Computer Aided Design (CAD) Laboratory (Pro-E), Nanotechnology Laboratory, Advanced FRP Laboratory Advanced Modeling Laboratory</p>



<p>Major Project Areas. There are ample opportunities to get involved in projects listed below but not restricted to: Design and Analysis of Mechanisms , Structural Analysis of Complex Systems (Aerospace, Automotive, Naval, etc), Dynamic Behavior [Impact, Damping, Buckling] of Systems , Thermal Analysis of systems, Fatigue and Fracture Analysis, Design of Macro, Micro and Nanosystems, Design and Analysis of Coating Systems</p>



