        <h1>B.E. in Mechanical Engineering</h1>

        <p>The Mechanical Engineering is concerned with the Production transmission and use of mechanical power and heat; it is the largest branch of Engineering. It is concerned with design, operation and maintenance of machines, their components, machine tools, manufacturing systems and processes and Industrial Engineering. Mechanical engineers are the highest employed engineers worldwide, it is called the evergreen engineering branch.</p>

<p>The Department has one of the finest Mechanical workshops and laboratories among the Engineering colleges in Bangalore, the laboratories setup are the Metallography and Material testing Laboratory, Mechanical Measurements and Metrology laboratory, Foundry and Forging Laboratory, Machine Shop, Fluid Mechanics and Machines Laboratory, Energy Conversion Engineering Laboratory, Heat & Mass Transfer Laboratory and Computer Aided Modeling and Analysis Laboratory. The department has a 1000 KN(100 Ton) Krystal Universal Material Testing Machine which only few Engineering colleges in Bangalore possess. The Machine Shop is equipped with the excellent HIMAC-Lx 175 All Geared Head Lathe Machines, Milling and Shaping machines from Mac Power Industries. The Department encourages students to present technical papers at Conferences.</p>





