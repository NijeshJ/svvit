        <h1>Department of Civil Engineering</h1>

        <p>The Department of Civil Engineering in VVIT was established in the year 2009. Over the years, it has grown into one of the finest centers of learning in the field of civil engineering. The Department aims to bring out engineers equipped with the latest technical skills & global exposure who are ready to handle any sort of practical issue with ease with their solid theoretical background. The Department has been very proactive in industrial interaction and technology development. It has built excellent experimental and computational facilities related to Hydraulics, Geotechnical, Structural and Environmental Engineering fields. </p>

        <h3>Vision</h3>

          <p><strong>"To be a center of Excellence in the field of Civil Engineering imparting quality technical education with active involvement in Applied Research & Consultancy catering to the social and technological needs of mankind."</strong></p>

        <h3>Mission</h3>

        <ul>

          <li>To produce world-class Civil Engineers with strong technical background and a practical approach to solve the infrastructure needs of the world.</li>

          <li>To produce Industry-ready workforce with high professional & ethical standards along with a strong entrepreneurial spirit.</li>

          <li>To carry out state-of-the art research relevant to global challenges in Civil Engineering & society at large.</li>

          <li>To train & disseminate knowledge of Civil Engineering among people. </li>

          <li>To develop into a department offering consultancy services to solve the problems of the industry and engage with the industry.</li>

        </ul>

       

