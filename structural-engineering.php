        <h1>M.Tech in Structural Engineering</h1>



<p>Structural engineering is a field of engineering dealing with the analysis and design of structures that support or resist loads. Structural engineering is usually considered a specialty within civil engineering, but it can also be studied in its own right. Structural engineers are most commonly involved in the design of buildings and large non-building structures but they can also be involved in the design of machinery, medical equipment, vehicles or any item where structural integrity affects the item's function or safety.</p>



<p>Structural engineering theory is based upon physical laws and empirical knowledge of the structural performance of different materials and geometries. Structural engineering design utilizes a number of simple structural elements to build up structural systems that can be very complex. Structural engineers are responsible for making creative and efficient use of funds, structural elements and materials to achieve these goals.</p>



<p>Structural engineers are responsible for engineering design and analysis. Entry-level structural engineers may design the individual structural elements of a structure, for example the beams, columns, and floors of a building. More experienced engineers would be responsible for the structural design and integrity of an entire system, such as a building.</p>



<p>This course will help the student to have in-depth understanding of structural behavior. This understanding will form a basis for analysis and design of building structures. It will encourage using information technology in the design process. Further more, the students will obtain the ability to choose and adapt calculation models and appropriate methods on basis of in-depth understanding of the physical behavior, properties and limitations of the models and with regard to the actual purpose of the calculations. Career opportunities are very bright in a location like Bangalore and the remunerations for structural engineers are very high in Civil Engineering Consultancy and Construction Companies.</p>

