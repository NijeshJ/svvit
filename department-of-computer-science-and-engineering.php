        <h1>Department of Computer Science and Engineering</h1>

            <p>The Department of Computer Science & Engineering in VVIT was established in 2009 & it currently offers an undergraduate course in Computer Science & Engineering with an intake of 60 students and post graduate course in Computer Network Engineering with an intake of 18.</p>

<p>Computers have become an indispensible part of our life and an aide to solving problems of any magnitude. This is where the young Computer Engineers step in to create a difference. Career opportunities for students include developing software systems for a diverse range of applications, such as user interfaces, networks, databases, forecasting, World Wide Web support, medical communication, satellite, embedded systems, artificial intelligence and neural networks.</p>

<p>The faculty members of the department have extensive industry and teaching experience, hence enabling them to impart an amalgamated teaching technique that will prepare the students for the tough industry benchmarks. The department has a large collection of reference books and software packages. The impact of our ideas and our students extend globally in meaningful and effective ways.</p>

<p>We have complete fledged labs according to the academic plans of VTU. The laboratories have modern computing facilities with the latest branded desktops, and three 5Mbps Internet lines which aid the students in research and referencing.</p>



<h3>Vision</h3>

<p><strong>"CSE Department has pledged to produce well trained Engineering Graduates & Post Graduates and to orient the education to suit technical manpower needs of the nation."</strong></p>



